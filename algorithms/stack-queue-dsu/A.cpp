using namespace std;

class intStack {
private:
    vector<int> st;
public:
    void push(int el) {
        st.push_back(el);
    }

    void pop() {
        st.pop_back();
    }

    int top() {
        return st.back();
    }

    int size() {
        return st.size();
    }
};

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int n;
    cin >> n;
    intStack mn;
    while (n--) {
        int cmd;
        cin >> cmd;
        if (cmd == 1) {
            int el;
            cin >> el;
            if (!mn.size() || el <= mn.top()) {
                mn.push(el);
            } else {
                mn.push(mn.top());
            }
        } else if (cmd == 2) {
            mn.pop();
        } else {
            cout << mn.top() << "\n";
        }
    }

    return 0;
}