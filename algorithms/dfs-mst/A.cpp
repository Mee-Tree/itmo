using namespace std;

vector<vector<int>> g;
vector<char> color;

bool dfs(int u, vector<int>& topsort) {
    color[u] = 1;

    for (int to : g[u]) {
        if (color[to] == 0) {
            if (dfs(to, topsort)) {
                return true;
            }
        } else if (color[to] == 1) {
            return true;
        }
    }

    color[u] = 2;
    topsort.emplace_back(u);
    return false;
}

void solve() {
    int n, m;
    cin >> n >> m;

    g.resize(n + 1);
    color.resize(n + 1);
    for (int u, v; m-- && cin >> u >> v; ) {
        g[u].emplace_back(v);
    }

    vector<int> topsort;
    for (int i = 1; i <= n; ++i) {
        if (color[i] != 0) continue;
        if (dfs(i, topsort)) {
            cout << -1;
            return;
        }
    }
    reverse(topsort.begin(), topsort.end());

    for (int u : topsort) {
        cout << u << " ";
    }
}

int main() {
#ifdef LOCAL
    const string FILENAME = "req";
    freopen((FILENAME + ".in").c_str(), "r", stdin);
    freopen((FILENAME + ".out").c_str(), "w", stdout);
#endif
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    solve();
    return 0;
}


