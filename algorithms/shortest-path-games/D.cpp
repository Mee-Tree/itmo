using namespace std;
using ll = long long;
using pii = pair<int, int>;
using pill = pair<int, ll>;

const ll INF = 1e18 + 1337;

namespace {

struct edge {
    int u, v, w;

    edge(int u, int v, int w)
        : u(u), v(v), w(w) {}
};

void solve() {
    int n, m, k, s;
    cin >> n >> m >> k >> s;

    vector<edge> edges;
    vector<ll> d(n, INF);
    for (int u, v, w; m-- && cin >> u >> v >> w;) {
        edges.emplace_back(--u, --v, w);
    }

    d[--s] = 0;
    for (int i = 1; i <= k; ++i) {
        vector<ll> nd(n, INF);
        for (auto [u, v, w] : edges) {
            if (d[u] < INF) {
                nd[v] = min(nd[v], d[u] + w);
            }
        }
        d = nd;
    }

    for (ll a : d) {
        cout << (a < INF ? a : -1) << endl;
    }
}

} // anonymous

signed main() {
#ifdef LOCAL
    const string FILENAME = "local";
    freopen((FILENAME + ".in").c_str(), "r", stdin);
    freopen((FILENAME + ".out").c_str(), "w", stdout);
#endif
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    solve();
    return 0;
}


