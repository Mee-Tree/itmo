using namespace std;

const int INF = 1e9 + 228;
const int MOD = 1e9;

signed main() {
    string first, second;
    cin >> first >> second;
    int n = first.length();
    int m = second.length();

    vector<vector<int>> dp(n + 1, vector<int>(m + 1));
    for (int i = 1; i <= n; ++i) dp[i][0] = i;
    for (int i = 1; i <= m; ++i) dp[0][i] = i;

    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            if (first[i - 1] == second[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            } else {
                dp[i][j] = 1 + min({
                    dp[i - 1][j - 1],
                    dp[i][j - 1],
                    dp[i - 1][j]
                });
            }
        }
    }
    cout << dp[n][m];

    return 0;
}