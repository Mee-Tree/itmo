using namespace std;

const int INF = 1e9 + 228;
const int MOD = 1e9;

signed main() {
    int n, m;
    cin >> n >> m;
    if (n < m) swap(n, m);

    vector<vector<int>> dp(n + 1, vector<int>(1 << m));
    int alt = 0;
    for (int i = 1; alt + i < (1 << m); i *= 4) alt += i;
    dp[0][alt] = 1;

    for (int i = 0; i < n; ++i) {
        for (int p = 0; p < (1 << m); ++p) {
            for (int q = 0; q < (1 << m); ++q) {
                int np = ~p & ((1 << m) - 1);
                int nq = ~q & ((1 << m) - 1);
                if (!((p << 1) & p & (q << 1) & q || (np << 1) & np & (nq << 1) & nq)) {
                    dp[i + 1][q] += dp[i][p];
                }
            }
        }
    }

    int res = 0;
    for (int p = 0; p < (1 << m); ++p) {
        res += dp[n][p];
    }
    cout << res;

    return 0;
}