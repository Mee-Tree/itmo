using namespace std;

#define int long long

const int INF = 1e9 + 228;
const int MOD = 1e9;

signed main() {
    int n;
    cin >> n;
    // 1 {6, 8}
    // 2 {7, 9}
    // 3 {4, 8}
    // 4 {0, 3, 9}
    // 5
    // 6 {0, 1, 7}
    // 7 {2, 6}
    // 8 {1, 3}
    // 9 {2, 4}
    // 0 {4, 6}
    vector<vector<int>> dp(10, vector<int>(n, 0));
    for (int i = 0; i < 10; ++i) dp[i][0] = 1;

    dp[0][0] = 0;
    dp[8][0] = 0;
    for (int i = 1; i < n; ++i) {
        dp[0][i] = (dp[6][i - 1] + dp[4][i - 1]) % MOD;
        dp[1][i] = (dp[6][i - 1] + dp[8][i - 1]) % MOD;
        dp[2][i] = (dp[7][i - 1] + dp[9][i - 1]) % MOD;
        dp[3][i] = (dp[4][i - 1] + dp[8][i - 1]) % MOD;
        dp[4][i] = (dp[0][i - 1] + dp[3][i - 1] + dp[9][i - 1]) % MOD;
        dp[6][i] = (dp[0][i - 1] + dp[1][i - 1] + dp[7][i - 1]) % MOD;
        dp[7][i] = (dp[2][i - 1] + dp[6][i - 1]) % MOD;
        dp[8][i] = (dp[1][i - 1] + dp[3][i - 1]) % MOD;
        dp[9][i] = (dp[2][i - 1] + dp[4][i - 1]) % MOD;
    }

    int res = 0;
    for (int i = 0; i < 10; ++i) {
        res = (res + dp[i][n - 1]) % MOD;
    }
    cout << res;

    return 0;
}