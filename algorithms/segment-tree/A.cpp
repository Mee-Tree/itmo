using namespace std;
using ll = long long;

const ll INF = 2e18;
const ll MOD1 = 1 << 16;
const ll MOD2 = 1 << 30;

signed main() {
    int n, x, y, a;
    cin >> n >> x >> y >> a;

    vector<ll> sum(n + 1);
    for (int i = 1; i <= n; ++i) {
    	sum[i] = sum[i - 1] + a;
    	a = (1LL * x * a + y) % MOD1;
    }

    int m, z, t, b;
    cin >> m >> z >> t >> b;

    ll res = 0;
    for (int i = 0; i < m; ++i) {
    	int c1 = b % n;
    	b = (1LL * z * b + t + MOD2) % MOD2;
    	int c2 = b % n;
    	b = (1LL * z * b + t + MOD2) % MOD2;
    	if (c1 > c2) {
    		swap(c1, c2);
    	}
    	res += sum[c2 + 1] - sum[c1];
    }
    cout << res;

	return 0;
}
