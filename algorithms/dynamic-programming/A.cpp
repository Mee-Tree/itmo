using namespace std;

const int INF = 1e9 + 228;

signed main() {
    int n, k;
    cin >> n >> k;

    vector<int> coins(n);
    for (int i = 1; i < n - 1; ++i) {
        cin >> coins[i];
    }

    vector<int> dp(n);
    vector<int> prev(n);
    prev[0] = -1;
    for (int i = 1; i < n; ++i) {
        int mx = i - 1;
        for (int j = 1; j <= min(k, i); ++j) {
            if (dp[mx] < dp[i - j]) {
                mx = i - j;
            }
        }
        dp[i] = dp[mx] + coins[i];
        prev[i] = mx;
    }
    cout << dp[n - 1] << "\n";

    vector<int> ans;
    for (int i = n - 1; i >= 0; i = prev[i]) {
        ans.push_back(i + 1);
    }

    cout << ans.size() - 1 << "\n";
    for (int i = (int)ans.size() - 1; i >= 0; --i) {
        cout << ans[i] << " ";
    }

    return 0;
}