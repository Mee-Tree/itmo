using namespace std;

#define int long long

const int INF = 1e9 + 228;
const int MOD = 1e9;

signed main() {
    int n, m;
    cin >> n >> m;

    int cnt = 0;
    vector<string> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        for (int j = 0; j < m; ++j) {
            cnt += (a[i][j] == 'X');
        }
    }

    vector<vector<vector<int>>> dp(n + 1, vector<vector<int>>(m + 1, vector<int>(1 << m)));
    dp[0][0][0] = 1;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            for (int p = 0; p < (1 << m); ++p) {
                if (a[i][j] == 'X') {
                    if (p & (1 << j)) {
                        dp[i][j][p] = 0;
                    } else {
                        dp[i][j + 1][p] += dp[i][j][p];
                    }
                    continue;
                }
                dp[i][j + 1][p ^ (1 << j)] += dp[i][j][p];

                if (j + 1 < m && !(p & (3 << j))) {
                    dp[i][j + 1][p + (1 << (j + 1))] += dp[i][j][p];
                }
            }
        }

        for (int p = 0; p < (1 << m); ++p) {
            dp[i + 1][0][p] += dp[i][m][p];
        }
    }
    cout << (cnt == n * m ? 0 : dp[n][0][0]);

    return 0;
}