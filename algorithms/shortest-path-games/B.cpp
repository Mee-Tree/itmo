using namespace std;
using ll = long long;
using pii = pair<int, int>;
using pill = pair<int, ll>;

const ll INF = 2e9 + 1337;

void solve() {
    int n, m;
    cin >> n >> m ;

    vector<vector<pii>> g(n);
    for (int u, v, w; m-- && cin >> u >> v >> w;) {
        g[--u].emplace_back(--v, w);
        g[v].emplace_back(u, w);
    }

    vector<bool> used(n);
    vector<ll> d(n, INF);

    d[0] = 0;
    set<pill> q{{d[0], 0}};

    while (!q.empty()) {
        auto [_, u] = *q.begin();
        q.erase(q.begin());

        for (auto [to, w] : g[u]) {
            if (d[to] > d[u] + w) {
                q.erase({d[to], to});
                d[to] = d[u] + w;
                q.emplace(d[to], to);
            }
        }
    }

    for (int i = 0; i < n; ++i) {
        cout << d[i] << " ";
    }
}

signed main() {
#ifdef LOCAL
    const string FILENAME = "req";
    freopen((FILENAME + ".in").c_str(), "r", stdin);
    freopen((FILENAME + ".out").c_str(), "w", stdout);
#endif
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    solve();
    return 0;
}


