using namespace std;

class intStack {
private:
    vector<int> st;
public:
    void push(int el) {
        st.push_back(el);
    }

    void pop() {
        st.pop_back();
    }

    int top() {
        return st.back();
    }

    int size() {
        return st.size();
    }
};

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    char c;
    intStack st;
    while (cin >> c) {
        if (c == '*') {
            int a = st.top();
            st.pop();
            int b = st.top();
            st.pop();
            st.push(a * b);
        } else if (c == '+') {
            int a = st.top();
            st.pop();
            int b = st.top();
            st.pop();
            st.push(a + b);
        } else if (c == '-') {
            int a = st.top();
            st.pop();
            int b = st.top();
            st.pop();
            st.push(b - a);
        } else {
            st.push((int)(c - '0'));
        }
    }
    cout << st.top();

    return 0;
}